    public FormularioDevoluciones(ListadoDevoluciones parent, boolean modal, String mode, int id) {
        super(parent, modal);
        this.parent = parent;
        this.mode = mode;
        this.id_devolucion = id;

        initComponents();
        setTitle("Formulario de Devoluciones");

        dao = new DevolucionDAO();
        txt_id.setEnabled(false);

        if (mode.equals("INS")) {
            btn_guardar.setText("Insertar");
        } else if (mode.equals("UPD")) {
            Devolucion d = (Devolucion) dao.getById(id_devolucion);
            if (d != null) {
               txt_id.setText(String.valueOf(d.getId_devolucion()));
                txt_iddevolucion.setText(String.valueOf(d.getId_devolucion()));
                txt_idprestamo.setText(String.valueOf(d.getId_prestamo()));
                txt_idlibro.setText(String.valueOf(d.getId_libro()));
                txt_fechaDevolucion.setText(d.getFecha_devolucion().toString());
                btn_guardar.setText("Actualizar");
            }
        } else if (mode.equals("DLT")) {
            Devolucion d = (Devolucion) dao.getById(id_devolucion);
            if (d != null) {
                txt_id.setText(String.valueOf(d.getId_devolucion()));
                txt_id.setEnabled(false);
                txt_iddevolucion.setText(String.valueOf(d.getId_devolucion()));
                txt_iddevolucion.setEnabled(false);
                txt_idprestamo.setText(String.valueOf(d.getId_prestamo()));
                txt_idprestamo.setEnabled(false);
                txt_idlibro.setText(String.valueOf(d.getId_libro()));
                txt_idlibro.setEnabled(false);
                txt_fechaDevolucion.setText(d.getFecha_devolucion().toString());
                txt_fechaDevolucion.setEnabled(false);
                btn_guardar.setText("Eliminar");
            }
        }
    }

