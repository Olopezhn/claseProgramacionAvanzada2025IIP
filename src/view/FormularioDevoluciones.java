    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed

           try {
        int idPrestamo = Integer.parseInt(txt_idprestamo.getText());
        int idLibro = Integer.parseInt(txt_idlibro.getText());
        Date fechaDevolucion;
        try {
       fechaDevolucion = Date.valueOf(txt_fechaDevolucion.getText());
       } catch (IllegalArgumentException e) {
        JOptionPane.showMessageDialog(this, "Formato de fecha inválido. Use AAAA-MM-DD.");
        return;
     }

        DevolucionDAO dao = new DevolucionDAO();

        if (dao.getByIdPrestamo(idPrestamo) != null) {
            JOptionPane.showMessageDialog(this, "Este préstamo ya fue devuelto.");
            return;
        }

        Devolucion devolucion = new Devolucion();
        devolucion.setId_prestamo(idPrestamo);
        devolucion.setId_libro(idLibro);
        devolucion.setFecha_devolucion(fechaDevolucion);

        boolean exito = dao.insert(devolucion);

        if (exito) {
            JOptionPane.showMessageDialog(this, "Devolución registrada correctamente.");
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Error al registrar devolución.");
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_btn_guardarActionPerformed

