    private void cargarDatos(){
        modeloTabla.setRowCount(0);
        List<Object> libro = librosDAO.getAll();
        
        for(Object libro_uncast : libro){
            Libros libros = (Libros) libro_uncast;
            modeloTabla.addRow(new Object[]{libros.getId_Libro(),libros.getTitulo_Libro(),libros.getId_Categoria() + " - " +libros.getnombre_Categoria(),libros.getEdicion(),libros.getEditorial(),libros.getFecha_Publicacion()});
        }
    }
    
    public void refrescarInfo(){
        cargarDatos();
    }

