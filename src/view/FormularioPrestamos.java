    private void btn_accionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_accionActionPerformed
         try {
            Prestamos prestamo = new Prestamos();
            if (!mode.equals("INS")) prestamo.setId_Prestamo(id_prestamo);
            prestamo.setId_Libro(Integer.parseInt(txt_idLibro.getText()));
            prestamo.setCantidad(Integer.parseInt(txt_cantidad.getText()));
            prestamo.setId_Usuario(Integer.parseInt(txt_idUsuario.getText()));
            prestamo.setId_Empleado(Integer.parseInt(txt_idEmpleado.getText()));
            Date fechaPrestamo;
         try {
           fechaPrestamo = Date.valueOf(txt_fechaPrestamo.getText());
         } catch (IllegalArgumentException e) {
           JOptionPane.showMessageDialog(this, "Formato de fecha inválido. Use AAAA-MM-DD en Fecha Préstamo.");
           return;
}

           Date fechaLimite;
         try {
          fechaLimite = Date.valueOf(txt_fechaLimite.getText());
         } catch (IllegalArgumentException e) {
          JOptionPane.showMessageDialog(this, "Formato de fecha inválido. Use AAAA-MM-DD en Fecha Límite.");
          return;
}

prestamo.setFecha_Prestamo(fechaPrestamo);
prestamo.setFecha_limite(fechaLimite);

            switch (mode) {
                case "INS" -> dao.insert(prestamo);
                case "UPD" -> dao.update(prestamo);
                case "DLT" -> dao.delete(id_prestamo);
            }

            parent.refrescarInfo();
            this.dispose();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
        
    }//GEN-LAST:event_btn_accionActionPerformed

