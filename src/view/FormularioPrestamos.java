    public FormularioPrestamos(ListadoPrestamos pParent, boolean modal, String pMode, int pId_prestamo) {
        super(pParent, modal);
        initComponents();
        this.parent = pParent;
        this.dao = new PrestamoDAO();
        this.mode = pMode;
        this.id_prestamo = pId_prestamo;
        setTitle("Formulario de Préstamos");

        txt_id.setText(String.valueOf(pId_prestamo));
        txt_id.setEnabled(false);

        if (mode.equals("INS")) {
            btn_accion.setText("Insertar");
        } else {
            cargarDatos();
            if (mode.equals("UPD")) {
                btn_accion.setText("Actualizar");
            } else if (mode.equals("DLT")) {
                btn_accion.setText("Eliminar");
                deshabilitarCampos();
            }
        }
    }

    private void cargarDatos() {
        Prestamos p = dao.getById(id_prestamo);
        if (p != null) {
            txt_idLibro.setText(String.valueOf(p.getId_Libro()));
            txt_cantidad.setText(String.valueOf(p.getCantidad()));
            txt_idUsuario.setText(String.valueOf(p.getId_Usuario()));
            txt_idEmpleado.setText(String.valueOf(p.getId_Empleado()));
            txt_fechaPrestamo.setText(p.getFecha_Prestamo().toString());
            txt_fechaLimite.setText(p.getFecha_limite().toString());
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo cargar el préstamo.");
        }
    }

    private void deshabilitarCampos() {
        txt_idLibro.setEnabled(false);
        txt_cantidad.setEnabled(false);
        txt_idUsuario.setEnabled(false);
        txt_idEmpleado.setEnabled(false);
        txt_fechaPrestamo.setEnabled(false);
        txt_fechaLimite.setEnabled(false);
    }


