    private void btn_accionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_accionActionPerformed
        // TODO add your handling code here:
        if(mode.equals("INS")){
            Usuarios usuario = new Usuarios();
            usuario.setdniUsuario(txt_dni.getText());
            usuario.setnombreUsuario(txt_nombre.getText());
            usuario.settipo_Usuario(Integer.parseInt(txt_tipo.getText()));
            usuario.setcorreo(txt_nombre.getText());
            usuario.settelefono(txt_nombre.getText());           
            
            
                                   
           dao.insert(usuario);           
           parent.refrescarInfo();
           this.dispose();
        }
        if(mode.equals("UPD")){
            Usuarios usuario = new Usuarios();
            usuario.setIdUsuario(Integer.parseInt(txt_id.getText()));
            usuario.setdniUsuario(txt_dni.getText());
            usuario.setnombreUsuario(txt_nombre.getText());
            usuario.settipo_Usuario(Integer.parseInt(txt_tipo.getText()));
            usuario.setcorreo(txt_correo.getText());
            usuario.settelefono(txt_telefono.getText());
                           
            dao.update(usuario);           
            parent.refrescarInfo();
            this.dispose();
        }
        
        if(mode.equals("DLT")){              
           dao.delete(id_usuario);           
           parent.refrescarInfo();
           this.dispose();
        }
        
    }//GEN-LAST:event_btn_accionActionPerformed

