    public FormularioUsuarios(ListadoUsuarios pParent, boolean modal, String pMode, int pId_Usuario) {
        super(pParent, modal);
        this.parent = pParent;
        initComponents();
        
        permitirSoloNumeros(txt_tipo);
        
        dao = new UsuariosDAO();
        this.mode = pMode;
        this.id_usuario = pId_Usuario;
        txt_id.setText(String.valueOf(pId_Usuario));
        
        if(mode.equals("INS")){
            setTitle("Formulario Insertar");
            btn_accion.setText("Insertar");
        }else if(mode.equals("UPD")){
            setTitle("Formulario Actualizar");
            Usuarios usuario = (Usuarios) dao.getById(id_usuario);
            txt_dni.setText(usuario.getdni_Usuario());
            txt_nombre.setText(usuario.getnombre_Usuario());
            txt_tipo.setText(String.valueOf(usuario.gettipo_Usuario()));
            txt_correo.setText(usuario.getcorreo());
            txt_telefono.setText(usuario.gettelefono());
            
            btn_accion.setText("Actualizar");            
        }
        else if(mode.equals("DLT")){
            setTitle("Formulario Eliminar");
            Usuarios usuario = (Usuarios) dao.getById(id_usuario);
            txt_dni.setText(usuario.getdni_Usuario());
            txt_dni.setEnabled(false);
            
            txt_nombre.setText(String.valueOf(usuario.getnombre_Usuario()));
            txt_nombre.setEnabled(false);
            
            txt_tipo.setText(String.valueOf(usuario.gettipo_Usuario()));
            txt_tipo.setEnabled(false);         
            
            txt_correo.setText(usuario.getcorreo());
            txt_correo.setEnabled(false);
            
            txt_telefono.setText(String.valueOf(usuario.gettelefono()));
            txt_telefono.setEnabled(false);
            
            
            btn_accion.setText("Eliminar");            
        }
    }

