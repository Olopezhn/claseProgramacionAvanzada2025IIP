    private void btn_ActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ActualizarActionPerformed

         int fila = tbl_devoluciones.getSelectedRow();
        if (fila >= 0) {
            int id_devolucion = Integer.parseInt(tbl_devoluciones.getValueAt(fila, 0).toString());
            FormularioDevoluciones form = new FormularioDevoluciones(this, true, "UPD", id_devolucion);
            form.setVisible(true);
            cargarDatos();
        } else {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un registro primero.");
        }
     
    }//GEN-LAST:event_btn_ActualizarActionPerformed

    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed

     int fila = tbl_devoluciones.getSelectedRow();
        if (fila >= 0) {
            int id_devolucion = Integer.parseInt(tbl_devoluciones.getValueAt(fila, 0).toString());
            int confirm = JOptionPane.showConfirmDialog(this, "¿Está seguro de eliminar esta devolución?", "Confirmar", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                dao.delete(id_devolucion);
                cargarDatos();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un registro primero.");
        }
        
    }//GEN-LAST:event_btn_deleteActionPerformed

