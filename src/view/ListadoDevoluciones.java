    private void cargarDatos() {
    modeloTabla.setRowCount(0);
        List<Object> devoluciones = dao.getAll();
        for (Object devolucionObj : devoluciones) {
            Devolucion d = (Devolucion) devolucionObj;
            modeloTabla.addRow(new Object[]{
               d.getId_devolucion(),
               d.getId_prestamo(),
               d.getId_libro(),
               d.getFecha_devolucion()
            });
        }
}
    
    public void refrescarInfo(){
        cargarDatos();
    }

