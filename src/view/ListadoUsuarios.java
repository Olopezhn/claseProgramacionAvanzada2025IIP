    private void cargarDatos(){
        modeloTabla.setRowCount(0); 
        List<Object> usuario = usuarioDAO.getAll();
        for(Object usuario_uncast : usuario){
            Usuarios usuarios = (Usuarios) usuario_uncast;           
           modeloTabla.addRow(new Object[]{usuarios.getId_Usuario(),
               usuarios.getdni_Usuario(),
               usuarios.getnombre_Usuario(),usuarios.gettipo_Usuario() + " - " + usuarios.getntipo_Usuario(),
               usuarios.getcorreo(),usuarios.gettelefono()});
        }
    }
    
    public void refrescarInfo(){
        cargarDatos();
    }

